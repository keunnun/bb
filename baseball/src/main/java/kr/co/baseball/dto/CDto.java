package kr.co.baseball.dto;

public class CDto {
	private int gnum, year, month, day;
	private String ext, hxt, gtime, parea, exscore, hxscore, reason,hxresult;
	public String getHxresult() {
		return hxresult;
	}
	public void setHxresult(String hxresult) {
		this.hxresult = hxresult;
	}
	public int getGnum() {
		return gnum;
	}
	public void setGnum(int gnum) {
		this.gnum = gnum;
	}
	public int getYear() {
		return year;
	}
	public void setYear(int year) {
		this.year = year;
	}
	public int getMonth() {
		return month;
	}
	public void setMonth(int month) {
		this.month = month;
	}
	public int getDay() {
		return day;
	}
	public void setDay(int day) {
		this.day = day;
	}
	public String getExt() {
		return ext;
	}
	public void setExt(String ext) {
		this.ext = ext;
	}
	public String getHxt() {
		return hxt;
	}
	public void setHxt(String hxt) {
		this.hxt = hxt;
	}
	public String getGtime() {
		return gtime;
	}
	public void setGtime(String gtime) {
		this.gtime = gtime;
	}
	public String getParea() {
		return parea;
	}
	public void setParea(String parea) {
		this.parea = parea;
	}
	public String getExscore() {
		return exscore;
	}
	public void setExscore(String exscore) {
		this.exscore = exscore;
	}
	public String getHxscore() {
		return hxscore;
	}
	public void setHxscore(String hxscore) {
		this.hxscore = hxscore;
	}
	public String getReason() {
		return reason;
	}
	public void setReason(String reason) {
		this.reason = reason;
	}
}
